// Package domain содержит основные доменные сущности и интерфейсы приложения.
package domain

import (
	"context"
	"errors"
)

// ErrNoteNotFound возвращается, когда заметка не найдена в базе данных.
var ErrNoteNotFound = errors.New("note not found")

// NoteRepository определяет интерфейс для работы с хранилищем заметок.
// Реализация этого интерфейса должна обеспечивать персистентность данных.
//
// Пример использования:
//
//	var repo domain.NoteRepository = repository.NewSQLiteRepository(db)
//	note, err := repo.Create(ctx, &domain.Note{Title: "Test"})
type NoteRepository interface {
	// Create создает новую заметку в хранилище.
	// Автоматически устанавливает ID, CreatedAt и UpdatedAt.
	// Возвращает созданную заметку с заполненными полями.
	//
	// Параметры:
	//   - ctx: контекст для отмены операции
	//   - note: заметка для создания (ID игнорируется)
	//
	// Возвращает:
	//   - *Note: созданная заметка с ID и временными метками
	//   - error: ошибка при создании (например, нарушение ограничений БД)
	Create(ctx context.Context, note *Note) (*Note, error)

	// GetByID возвращает заметку по её идентификатору.
	//
	// Параметры:
	//   - ctx: контекст для отмены операции
	//   - id: идентификатор заметки
	//
	// Возвращает:
	//   - *Note: найденная заметка
	//   - error: ErrNoteNotFound если заметка не найдена, или ошибка БД
	GetByID(ctx context.Context, id int64) (*Note, error)

	// GetAll возвращает все заметки с возможностью фильтрации.
	// Заметки возвращаются в порядке убывания даты создания (новые первые).
	//
	// Параметры:
	//   - ctx: контекст для отмены операции
	//   - filter: фильтр по категории (nil возвращает все заметки)
	//
	// Возвращает:
	//   - []*Note: список заметок (пустой список, если ничего не найдено)
	//   - error: ошибка при запросе к БД
	GetAll(ctx context.Context, filter *NoteFilter) ([]*Note, error)

	// Update обновляет существующую заметку.
	// Автоматически обновляет UpdatedAt.
	//
	// Параметры:
	//   - ctx: контекст для отмены операции
	//   - note: заметка с обновленными данными (ID должен быть установлен)
	//
	// Возвращает:
	//   - *Note: обновленная заметка
	//   - error: ErrNoteNotFound если заметка не найдена, или ошибка БД
	Update(ctx context.Context, note *Note) (*Note, error)

	// Delete удаляет заметку по идентификатору.
	//
	// Параметры:
	//   - ctx: контекст для отмены операции
	//   - id: идентификатор заметки для удаления
	//
	// Возвращает:
	//   - error: ErrNoteNotFound если заметка не найдена, или ошибка БД
	Delete(ctx context.Context, id int64) error
}
